prompt_2 = ChatPromptTemplate.from_template('다음 아이디어의 예상되는 기술적 문제점 2가지를 알려줘:{idea}')

chain_1 = prompt_1 | model | StrOutputParser() # parser_1
chain_2 = prompt_2 | model | StrOutputParser() # parser_2

# 첫번째 실행
idea = chain_1.invoke({'item': '홀로그램'})

# 첫번째 결과를 두번째 체인의 입력값으로 사용
problems = chain_2.invoke({'idea': idea})

# 결과 출력
